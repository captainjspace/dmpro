package dmpro.character;

/**
 * @author Joshua Landman, joshua.s.landman@gmail.com
 * created on Oct 27, 2016
 */
public class CharacterHistory {
	String characterId;
	String characterHistory;
	/**
	 * @return the characterId
	 */
	public String getCharacterId() {
		return characterId;
	}
	/**
	 * @param characterId the characterId to set
	 */
	public void setCharacterId(String characterId) {
		this.characterId = characterId;
	}
	/**
	 * @return the characterHistory
	 */
	public String getCharacterHistory() {
		return characterHistory;
	}
	/**
	 * @param characterHistory the characterHistory to set
	 */
	public void setCharacterHistory(String characterHistory) {
		this.characterHistory = characterHistory;
	}
}
